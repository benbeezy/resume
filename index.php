<h2>Dollo 3D printer</h2>
<p>I created the Dollo 3D as a proofe of consept, to show people that yes, a 3D printer can indeed print another 3D printer. Then I took it a lever more and made all of the models parametric, and the structure modular. So this printer can print its own extention pieces, to make its self larger, and it has a swappable head so that you can eithor laser-but CNC mill or 3D print with it,</p>
